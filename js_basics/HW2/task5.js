const radius = 5;
const areaCircle = (Math.PI * Math.pow(radius,2)).toFixed(2);
console.log(`Area of circle is ${areaCircle}`);

const length = 5;
const width = 6;
const areaSquare = (length * width).toFixed(2);
console.log(`Area of square is ${areaSquare}`);


const height = 10;
const cylinderRadius = 5;
const cylinderVolume = (Math.PI * Math.pow(cylinderRadius,2) * height).toFixed(2);
console.log(`Cylinder volume is ${cylinderVolume}`);